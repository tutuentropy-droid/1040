export const getMatchupsForPhilosopher = (philosopherId: string): DebateMatchup[] => {
  return debateMatchups.filter(
    (m) => m.philosopherA === philosopherId || m.philosopherB === philosopherId,
  );
};

export const getMatchupById = (id: string): DebateMatchup | undefined => {
  return debateMatchups.find((m) => m.id === id);
};

export const getAvailableOpponents = (philosopherId: string): string[] => {
  const opponents: string[] = [];
  debateMatchups.forEach((m) => {
    if (m.philosopherA === philosopherId) opponents.push(m.philosopherB);
    if (m.philosopherB === philosopherId) opponents.push(m.philosopherA);
  });
  return [...new Set(opponents)];
};

export const getMatchupBetween = (idA: string, idB: string): DebateMatchup | undefined => {
  return debateMatchups.find(
    (m) =>
      (m.philosopherA === idA && m.philosopherB === idB) ||
      (m.philosopherA === idB && m.philosopherB === idA),
  );
};
