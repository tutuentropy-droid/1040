export const getPhilosopherById = (id: string) =>
  philosopherNPCs.find((p) => p.id === id);

export const getPhilosophersByCategory = (category: string) =>
  philosopherNPCs.filter((p) => p.category === category);

export const getRandomPhilosopher = (exclude: string[] = []): PhilosopherNPC | null => {
  const available = philosopherNPCs.filter((p) => !exclude.includes(p.id));
  if (available.length === 0) return null;
  return available[Math.floor(Math.random() * available.length)];
};
