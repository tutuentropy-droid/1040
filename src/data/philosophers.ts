export const getPhilosopherById = (id: string): PhilosopherNPC | undefined => {
  return philosopherNPCs.find((p) => p.id === id);
};

export const getRandomPhilosopher = (excludeIds: string[] = []): PhilosopherNPC | undefined => {
  const available = philosopherNPCs.filter((p) => !excludeIds.includes(p.id));
  if (available.length === 0) return undefined;
  return available[Math.floor(Math.random() * available.length)];
};
